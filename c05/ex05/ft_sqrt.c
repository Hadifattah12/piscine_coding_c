int	ft_sqrt(int nb)
	{
	int	i;

	i = 0;
	if (nb == -2147483648)
	{
		return (0);
	}
	if (nb == 1)
	{
		return (1);
	}
	if (nb < 0)
	{
		return (-1 * ft_sqrt(-nb));
	}
	i = 0;
	while (i <= nb / 2 && i <= 46340)
	{
		if (i * i == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
